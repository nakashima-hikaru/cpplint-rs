/// Folders of C libraries commonly used in C++.
pub const C_STANDARD_HEADER_FOLDERS: &[&str] = &[
    "sys",
    "arpa",
    "asm-generic",
    "bits",
    "gnu",
    "net",
    "netinet",
    "protocols",
    "rpc",
    "rpcsvc",
    "scsi",
    "drm",
    "linux",
    "misc",
    "mtd",
    "rdma",
    "sound",
    "video",
    "xen",
];

pub fn get_header_folders_pattern() -> String {
    let mut pattern = String::from("(?:");
    for (i, folder) in C_STANDARD_HEADER_FOLDERS.iter().enumerate() {
        if i > 0 {
            pattern.push('|');
        }
        pattern.push_str(folder);
    }
    pattern.push_str(r")\/.*\.h");
    pattern
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_is_cpp_header() {
        assert!(CPP_HEADERS.contains(&"vector"));
        assert!(CPP_HEADERS.contains(&"algorithm"));
        assert!(!CPP_HEADERS.contains(&"stdio.h"));
    }

    #[test]
    fn test_is_c_header() {
        assert!(C_HEADERS.contains(&"stdio.h"));
        assert!(C_HEADERS.contains(&"unistd.h"));
        assert!(!C_HEADERS.contains(&"vector"));
    }

    #[test]
    fn test_get_header_folders_pattern() {
        let pattern = get_header_folders_pattern();
        assert!(pattern.contains("sys|arpa|asm-generic"));
        assert!(pattern.ends_with(r")\/.*\.h"));
    }
}
